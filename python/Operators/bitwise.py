if 13 & 14:
  print("hii")
else:
  print("hello")


if 13 | 14:
  print("hii")
else:
  print("hello")

if 13 >> 14:
  print("hii")
else:
  print("hello")


if 13 << 14:
  print("hii")
else:
  print("hello")


if 13 ^ 14:
  print("hii")
else:
  print("hello")

if ~13:
  print("hii")
else:
  print("hello")